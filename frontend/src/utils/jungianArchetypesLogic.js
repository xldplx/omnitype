const shuffle = (array) => {
  let currentIndex = array.length, randomIndex;
  while (currentIndex !== 0) {
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex--;
    [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
  }
  return array;
};

export const jungianQuestions = shuffle([...rawQuestions]);

export function calculateJungianResult(answers) {
  const scores = {};
  for (let i = 1; i <= 12; i++) {
    scores[i] = 0;
  }

  answers.forEach(answer => {
    // UI: Exactly Like Me (val: 1) -> Score 7
    // UI: Extremely Unlike Me (val: 7) -> Score 1
    const correctedScore = 8 - answer.value;
    scores[answer.type] += correctedScore;
  });

  // Find primary archetype
  let PrimaryType = 1;
  let maxScore = -1;

  for (const [type, score] of Object.entries(scores)) {
    if (score > maxScore) {
      maxScore = score;
      PrimaryType = parseInt(type);
    }
  }

  // Find secondary archetype
  let SecondaryType = 1;
  let secScore = -1;
  for (const [type, score] of Object.entries(scores)) {
    const t = parseInt(type);
    if (t !== PrimaryType && score > secScore) {
      secScore = score;
      SecondaryType = t;
    }
  }

  // Calculate percentage breakdown for the radar/bar charts
  const MAX_SCORE = 21; // 3 questions * 7 points max
  const breakdown = {};
  for (let i = 1; i <= 12; i++) {
    breakdown[i] = Math.max(0, Math.round((scores[i] / MAX_SCORE) * 100));
  }

  return {
    primaryInfo: jungianArchetypesMap[PrimaryType],
    secondaryInfo: jungianArchetypesMap[SecondaryType],
    breakdown: breakdown,
    scores: scores
  };
}
